# 4 funções com as operações básicas matemáticas

def soma():
    n1 = float(input("Primeiro numero: "))
    n2 = float(input("Segundo numero: "))
    print("Soma: ",n1+n2)

def subtracao():
    n1 = float(input("Primeiro numero: "))
    n2 = float(input("Segundo numero: "))
    print("Subtracao: ",n1-n2)

def multiplicacao():
    n1 = float(input("Primeiro numero: "))
    n2 = float(input("Segundo numero: "))
    print("Multiplicacao: ",n1*n2)

def divisao():
    n1 = float(input("Primeiro numero: "))
    n2 = float(input("Segundo numero: "))
    print("Divisao: ",n1/n2)

